class Matematica(object):
    def somar(valor1, valor2):
        '''
        Este metodo tem a funcionalidade de somar dois valores.

        Parametros:
        1. Valor1 (Number) - Primeira parcela
        2. Valor2 (Number) - Segunda parcela 

        Ex: Matematica.somar(1, 2)
        '''

        return valor1 + valor2

    def subtrair(valor1, valor2):
        '''
        Este metodo tem a funcionalidade de subtrair dois valores

        Parametros:
        1. Valor1 (Number) - Minuendo
        2. Valor2 (Number) - Subtraendo 

        Ex: Matematica.subtrair(1, 2)
        '''

        return valor1 - valor2